from typing import List
from queue import Queue
class Solution:
    #Function to return Breadth First Traversal of given graph.
    def bfsOfGraph(self, V: int, adj: List[List[int]]) -> List[int]:
        # code here
        ret = []
        stack = [0]
        while len(stack):
            cur = stack.pop(0)
            ret.append(cur)
            for node in adj[cur]:
                if node not in ret and node not in stack:
                    stack.append(node)
        return ret


